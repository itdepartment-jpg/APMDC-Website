<script>
    // Hero section functionality
    class Hero {
        constructor() {
            this.scrollIndicator = document.getElementById('scroll-indicator');
            this.ctaButtons = document.querySelectorAll('.hero-cta-btn, .hero-secondary-btn');
            this.init();
        }

        init() {
            this.bindEvents();
            this.startCounterAnimations();
        }

        bindEvents() {
            // Scroll indicator click
            if (this.scrollIndicator) {
                this.scrollIndicator.addEventListener('click', () => this.scrollToNextSection());
            }

            // CTA button interactions
            this.ctaButtons.forEach(button => {
                button.addEventListener('mouseenter', (e) => this.handleButtonHover(e));
                button.addEventListener('mouseleave', (e) => this.handleButtonLeave(e));
                button.addEventListener('click', (e) => this.handleButtonClick(e));
            });

            // Parallax effect for background elements
            window.addEventListener('scroll', () => this.handleParallax());
        }

        scrollToNextSection() {
            const nextSection = document.querySelector('#services') || document.querySelector(
                'section:nth-of-type(2)');
            if (nextSection) {
                nextSection.scrollIntoView({
                    behavior: 'smooth',
                    block: 'start'
                });
            }
        }

        handleButtonHover(e) {
            const button = e.currentTarget;
            button.style.transform = 'translateY(-2px)';
        }

        handleButtonLeave(e) {
            const button = e.currentTarget;
            button.style.transform = 'translateY(0)';
        }

        handleButtonClick(e) {
            const button = e.currentTarget;

            // Add click animation
            button.style.transform = 'scale(0.95)';
            setTimeout(() => {
                button.style.transform = 'scale(1)';
            }, 150);

            // Handle different button actions
            if (button.classList.contains('hero-cta-btn')) {
                this.handleGetStartedClick();
            } else if (button.classList.contains('hero-secondary-btn')) {
                this.handleWatchOverviewClick();
            }
        }

        handleGetStartedClick() {
            // Redirect to contact form or open modal
            const contactSection = document.getElementById('contact');
            if (contactSection) {
                contactSection.scrollIntoView({
                    behavior: 'smooth'
                });
            } else {
                // Fallback: show alert or redirect
                alert('Get Started clicked! This would typically open a contact form.');
            }
        }

        handleWatchOverviewClick() {
            // Open video modal or redirect to video page
            alert('Watch Overview clicked! This would typically open a video modal.');

            // Example: You can implement a video modal here
            // this.openVideoModal('https://www.youtube.com/embed/your-video-id');
        }

        openVideoModal(videoUrl) {
            // Implementation for video modal
            console.log('Opening video modal with URL:', videoUrl);
        }

        handleParallax() {
            const scrolled = window.pageYOffset;
            const parallaxElements = document.querySelectorAll(
                '.animate-float-1, .animate-float-2, .animate-float-3');

            parallaxElements.forEach((element, index) => {
                const speed = 0.5 + (index * 0.1);
                const yPos = -(scrolled * speed);
                element.style.transform =
                    `translateY(${yPos}px) ${element.style.transform.includes('rotate') ? element.style.transform.split('translateY')[1] : ''}`;
            });
        }

        startCounterAnimations() {
            // Animate stats counters when hero section is in view
            const observer = new IntersectionObserver((entries) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        this.animateCounters();
                        observer.unobserve(entry.target);
                    }
                });
            }, {
                threshold: 0.5
            });

            const heroSection = document.getElementById('home');
            if (heroSection) {
                observer.observe(heroSection);
            }
        }

        animateCounters() {
            const counters = document.querySelectorAll('.text-2xl.font-bold.text-accent');
            counters.forEach(counter => {
                const target = parseInt(counter.textContent);
                const duration = 2000; // 2 seconds
                const step = target / (duration / 16); // 60fps
                let current = 0;

                const timer = setInterval(() => {
                    current += step;
                    if (current >= target) {
                        counter.textContent = target + '+';
                        clearInterval(timer);
                    } else {
                        counter.textContent = Math.floor(current) + '+';
                    }
                }, 16);
            });
        }
    }

    // Initialize hero when DOM is loaded
    document.addEventListener('DOMContentLoaded', function() {
        new Hero();
    });
</script>
